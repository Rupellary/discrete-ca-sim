import numpy as np
import time
from typing import Dict, Tuple


def get_start(
    start_choice: str,
    shape_of_random: Tuple[int] = (18, 18)
) -> np.ndarray:
    """
    Uses global dictionary of starting states START_OPTIONS to convert input into numpy array.
    Generates random grid if choice is "randomize".
    
    Parameters
    ----------
    start_choice : str
        String corresponding with starting state selection. 
        May either be a key from the START_OPTIONS dict, "randomize", or "random_choice".
    shape_of_random : iterable of shape (2,)
        Only used when start_choice=="randomize".
        The shape of the randomly generated array.
    
    Returns
    ----------
    start : np.ndarray
        Binary numpy array to use as starting state for simulation
    """

    # Concatenate into full list of options
    start_options: list[str] = list(START_OPTIONS.keys()) + ['randomize']

    # Random choice randomly selects from options
    if start_choice == "random_choice":
        start_choice: str = np.random.choice(start_options)
        # Display which state has been chosen and pause for time to read
        print(f"Using {start_choice} starting state:")
        time.sleep(1)
    
    # Randomly generate binary grid
    if start_choice == "randomize":
        width, height = shape_of_random
        start: np.ndarray = np.array([[np.random.randint(0, 2) for _ in range(width)] for _ in range(height)])

    # Lookup in dictionary
    elif start_choice in START_OPTIONS.keys():
        start: np.ndarray = START_OPTIONS[start_choice]

    # If the input is none of these, raise a value error
    else: 
        raise ValueError("start-choice must be one of the valid options. Use --help to see what options are allowed")

    # Return starting state grid
    return start


